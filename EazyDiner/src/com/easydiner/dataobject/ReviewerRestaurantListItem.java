package com.easydiner.dataobject;

public class ReviewerRestaurantListItem {
	int itemId;
	String itemName, itemSpeciality, itemLocation, itemImg;
	public int getItemId() {
		return itemId;
	}
	public void setItemId(int itemId) {
		this.itemId = itemId;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getItemSpeciality() {
		return itemSpeciality;
	}
	public void setItemSpeciality(String itemSpeciality) {
		this.itemSpeciality = itemSpeciality;
	}
	public String getItemLocation() {
		return itemLocation;
	}
	public void setItemLocation(String itemLocation) {
		this.itemLocation = itemLocation;
	}
	public String getItemImg() {
		return itemImg;
	}
	public void setItemImg(String itemImg) {
		this.itemImg = itemImg;
	}
}
