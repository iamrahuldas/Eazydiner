package com.easydiner.dataobject;

public class ReservDetailsItem {
	private String itemName, itemDate, itemTime, itemPerson, itemLocation,
			itemDeal, itemConfirmNo, itemHoldingTime;

	private int id;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getItemName() {
		return itemName;
	}

	public void setItemName(String itemName) {
		this.itemName = itemName;
	}

	public String getItemDate() {
		return itemDate;
	}

	public void setItemDate(String itemDate) {
		this.itemDate = itemDate;
	}

	public String getItemTime() {
		return itemTime;
	}

	public void setItemTime(String itemTime) {
		this.itemTime = itemTime;
	}

	public String getItemPerson() {
		return itemPerson;
	}

	public void setItemPerson(String itemPerson) {
		this.itemPerson = itemPerson;
	}

	public String getItemLocation() {
		return itemLocation;
	}

	public void setItemLocation(String itemLocation) {
		this.itemLocation = itemLocation;
	}

	public String getItemDeal() {
		return itemDeal;
	}

	public void setItemDeal(String itemDeal) {
		this.itemDeal = itemDeal;
	}

	public String getItemConfirmNo() {
		return itemConfirmNo;
	}

	public void setItemConfirmNo(String itemConfirmNo) {
		this.itemConfirmNo = itemConfirmNo;
	}

	public String getItemHoldingTime() {
		return itemHoldingTime;
	}

	public void setItemHoldingTime(String itemHoldingTime) {
		this.itemHoldingTime = itemHoldingTime;
	}
}
